namespace EZPCBSCheats
{
    public partial class Hotkeys : MaterialForm
    {
        public Hotkeys()
        {
            InitializeComponent();
        }

        private void Hotkeys_FormClosing(object sender, FormClosingEventArgs e)
        {
            if (e.CloseReason == CloseReason.UserClosing)
            {
                e.Cancel = true;
                Hide();
            }
        }
    }
}
